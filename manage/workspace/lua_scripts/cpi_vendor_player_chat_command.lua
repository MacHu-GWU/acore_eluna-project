在这个例子中, 点击选项后会给玩家加 buff.
--]]
---@param player Player,
---@param itemGossipOption ItemGossipOptionType,
local function PlayerAction(
        player,
        itemGossipOption
)
    -- See: https://www.azerothcore.org/pages/eluna/Player/SendListInventory.html
    --local creature = player:GetMap():GetWorldObject(5000325)
    local unitGuid = GetUnitGUID( 5000325, 5005001 )
    print(string.format("unitGuid = %s", unitGuid))
    local creature = player:GetMap():GetWorldObject(unitGuid)
    print(string.format("creature = %s", creature))
    player:SendListInventory(
        creature,
        itemGossipOption.data.npc_id
    )
end

-- 定义玩家输入的命令
local command = "#cpi"
-- 定义 gossip menu 的 id, 要确保在有多个类似于这个 App 的情况下, 这个 id 是唯一的
local gossip_menu_id = 103003
-- 定义 gossip menu 的 npc_text_id
local npc_text_id = gossip_menu_utils.DEFAULT_NPC_TEXT_ID

-- 创建一个 PlayerChatCommandTreeGossipMenuHandler 的实例
CpiVendorPlayerChatCommand.TeleportHandler = player_chat_command_utils.PlayerChatCommandTreeGossipMenuHandler.new(
        CpiVendorPlayerChatCommand.gossipOptionList,
        command,
        gossip_menu_id,
        npc_text_id,
        PlayerAction
)

-- Register Events
CpiVendorPlayerChatCommand.TeleportHandler:RegisterEvents()
